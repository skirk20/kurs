instytucji. Następnie uczniowie prezentują wyniki pracy uzasadniając swoje
odpowiedzi.<br>
Podsumuj, że wartościami kierują się nie tylko pojedynczy ludzie, ale również grupy
i instytucje. Przykładem grupy, jednej z najważniejszych w społeczeństwie, jest
rodzina.
        </li>
</ol>
