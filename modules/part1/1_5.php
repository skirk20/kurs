        <h3>
        Faza realizacyjna [25 minut]
        </h3>
        <ol>
        <li>
        Każdemu uczniowi wręcz kartę wartości (załącznik nr 2). Poproś, aby wybrał
5 wartości, które uważa za najważniejsze i zastanowił się, dlaczego je wskazał.
        </li>
        <li>
        Stosując metodę kuli śnieżnej zachęć uczniów, aby porównali swoje odpowiedzi
najpierw z jedną, a następnie z kolejnymi dwiema osobami. Zapytaj, w jakim stopniu
ich wybór wartości był zbieżny ze wskazaniami kolegów i koleżanek.<br>
Podsumuj zadanie zwracając uwagę, że ludzie wyznają różne wartości i nadają im
różne znaczenie. Dodatkowo nasz system wartości zmienia się na przestrzeni czasu,
w zależności od tego, w jakim jesteśmy wieku, jakie zdobywamy doświadczenia
życiowe, w jakim środowisku funkcjonujemy. Ważne, aby przyglądać się sobie,
zastanawiać się, jakie wartości wpływają na nasze zachowanie. Jeśli jesteśmy tego
świadomi, lepiej kierujemy własnym życiem. Odwołaj się do konkretnych przykładów,
np. z własnego życia lub z życia klasy, szkoły.
        </li>
        <li>
        Wręcz grupom uczniów Opisy grup i instytucji (załącznik nr 3). Poproś, aby po
