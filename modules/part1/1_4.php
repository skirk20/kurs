<h2>Wiedza</h2>
<p>Zapewne znacznie łatwiej będzie pani Justynie zrozumieć uwagę nauczycielki po przeczytaniu
    wiadomości nr 1. Magda odniosła się do konkretnej wartości, na dodatek powszechnie
    uznanej za ważną. Oczywiście nie mamy pewności, że mama Adriana zmieni swoją postawę,
    czyli uzna, że tak szeroko pojęta wolność jest w tej sytuacji mniej ważna od szacunku, jednak
    będzie musiała się nad tym zastanowić, dokonać wyboru.</p>
    <p>
        Magda pisząc wiadomość nr 2 odwołała się wyłącznie do procedur. Mama Adriana, nie
rozumiejąc ich celu, może czuć potrzebę obrony swojego systemu wartości i jeszcze mocniej
bronić wolności wyboru swojego syna. A to już prosta droga do, zbędnego w tym przypadku,
konfliktu o wartości.
    </p>
    <p>
        Będąc wychowawcą, stajesz się liderem klasy. A ta, podobnie jak inne organizacje
(np. fundacje, korporacje, małe rodzinne przedsiębiorstwa, muzea) powinna mieć jasno
nazwane wartości. Dzięki temu uczniowie i rodzice będą rozumieli, co w waszej klasie
(szkole) jest ważne w zakresie trzech funkcji szkoły: dydaktyki, wychowywania i opieki.
    </p>
<h3>jak określić te wartości?</h3>
<ol>
    <li>Sprawdź, jakie wartości są zapisane w szkolnych dokumentach. Bardzo możliwe, że są
        one wyrażone wprost, np. w koncepcji pracy szkoły, programie profilaktyczno-
        wychowawczym lub statucie szkoły.</li>
    <li>Sprawdź wartości zapisane w prawie oświatowym – np. ustawie Prawo oświatowe,
        Karcie Nauczyciela (znajdziesz je w załączniku).</li>
    <li>Wsłuchaj się w głos rodziców i uczniów. Stwórz przestrzeń, w której będą mogli
        opowiedzieć o tym, co dla nich jest ważne. Możliwe, że sami zorientują się, że ich
        wartości stoją ze sobą w sprzeczności. Nie bój się tego. To świetna okazja do
        rozmowy o tym, jak trudno jest wychowywać, uczyć, kiedy różnimy się w sprawach
        fundamentalnych. Niech rodzice zobaczą jak duże musisz mieć kompetencje, żeby
        przewodzić tej organizacji.</li>
</ol>
<p>Wartości, które znajdziesz w prawie szkolnym lub oświatowym są obowiązujące, nie możesz
    ich zmienić. Są to wartości ogólnoludzkie, humanistyczne, które pomagają nam dobrze żyć
    i funkcjonować w społeczeństwie. Zostaje jeszcze spore pole do ich doprecyzowania. I to
    właśnie do tego zaproś uczniów i rodziców.</p>

<h3>Na czym mogą polegać różnice w wartościach?</h3>
<p>Od lat przy dyskusjach o edukacji wiele osób odwołuje się do fińskiego modelu kształcenia.
    W szczególności, że uczniowie w Finlandii osiągają bardzo dobre wyniki w badaniach PISA 1 .
    Nie dziwi więc, że rodzice spoglądają na fińską szkołę z rozmarzeniem – szkoła bez
    narzuconego systemu wymagań, nadmiernych prac domowych i testów, za to ucząca
    praktycznych umiejętności, zapewniająca autentyczną równowagę między nauką
    a wypoczynkiem; szkoła, w której uczeń może biegać w skarpetkach i spędzać przerwy na
    świeżym powietrzu. Jednak, to co jest kluczem fińskiej szkoły, co przewija się w rozmowach
    z fińskimi nauczycielami, to zaufanie. Stanowi ono wartość, na której zbudowano cały system
    edukacji 2 .</p>
