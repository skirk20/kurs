<h2>Wiedza</h2>
<p>To, jak na powyższe słowa docenienia zareagowali ich odbiorcy, w dużym stopniu zależy od
doświadczeń, przekonań, a przede wszystkim ich poczucia własnej wartości.</p>
<p>
Pani Joanna mogła ucieszyć się, że dyrektor ją pochwalił za to, że spełniła JEGO oczekiwania
i że w końcu jest z niej dumny. Pan Paweł mógł uznać, że sukces wycieczki to przede
wszystkim zasługa dużej dozy szczęścia, a nie jego pracy. W końcu „wszystko się udało”, a nie
„wszystko Pan zorganizował”. Janek po tej pochwale wie, że ma być posłuszny, bo wtedy
będzie nagroda w formie pochwały (Brawo!).
</p>
<h4>Co usłyszeli pozostali bohaterowie?</h4>
<p>
Pani Karolina wie, że dyrektor zauważył jej pracę, czas, który przeznaczyła na przygotowania
chóru, ale także jej postawę – cierpliwość i umiejętność zaangażowania rodziców. Pan Michał
czuje, że nauczycielka zauważyła jego kompetencje komunikacyjne oraz organizacyjne. Zaś
Robert jest świadomy, że wychowawczyni rozumie jego trudności z koncentracją oraz widzi
wysiłek, jaki wkłada, by im sprostać. Co więcej, jest ciekawa, co mu w tym pomogło.
</p>
<p>
Mądre docenianie motywuje do dalszej pracy, wspiera budowanie poczucia własnej
sprawczości i wartości. Jesper Juul pisze, że poczucie własnej wartości pochodzi z dwóch
źródeł – z bycia dostrzeganym i poczucia, że jest się cenionym 7 . Poczucie bycia dostrzeganym
oraz bycia cenionym w dużym stopniu dają nam odpowiednio sformułowane wyrazy
uznania.
</p>
<p>
Należy tutaj uważać, aby nie wpaść w pułapkę i nie pomylić adekwatnego poczucia własnej
wartości z wysoką samooceną.
</p>
<p>
Poczucie wartości jest jednym z fundamentalnych przekonań, na których bazują nasze
postawy wobec siebie i świata. Dzięki adekwatnie ukształtowanemu poczuciu własnej
wartości akceptuję siebie i wiem, że zasługuję na szacunek swój oraz innych, bez względu na
moje słabe czy mocne strony.
</p>
<p>
To właśnie adekwatne poczucie własnej wartości jest kluczem do bycia szczęśliwym
człowiekiem, umiejącym wyciągać wnioski ze swoich porażek. W przypadku niepowodzenia
prowadzi nas do wniosku, że wprawdzie tym razem coś słabo poszło, ale nie zmienia to
faktu, że jestem wartościowym człowiekiem i że jeśli następnym razem popracuję bardziej
albo zmienię metodę działania, to zrobię postęp.
</p>
<p>
Rodzice i nauczyciele często zamiast wzmacniać poczucie własnej wartości, wzmacniają
samoocenę. Robią to oceniając, chwaląc czy porównując. Chcą zmotywować do dalszej
pracy, a w rzeczywistości budują w dziecku przekonanie, że jest akceptowane czy kochane
tylko dlatego, że potrafi sprostać stawianym mu wymaganiom (Jaś, który był grzeczny na
apelu; pani Joanna, z której dumny jest dyrektor). W konsekwencji każda porażka czy popełniony błąd, obniżają samoocenę i podkopują poczucie kompetencji, a to może rodzić
obawę o samoakceptację i zniechęcać do podejmowania działań, których do tej pory nie
podejmowaliśmy.
</p>
<h4>
Jak zatem mądrze doceniać?
</h4>
<p>
Żeby móc docenić, trzeba wiedzieć, za co. Wymaga to czasu i uważności: patrzenia,
słuchania, rozmawiania. Musisz wiedzieć, co dokładnie chcesz docenić, i to jest największe
wyzwanie – dostrzec to i nazwać.
<ol>
    <li>
    Bądź autentyczny – doceniaj wtedy, kiedy wiesz i czujesz, za co.
    </li>
    <li>
    Odwołuj się do faktów – wskaż konkretnie zachowanie, pracę, wysiłek, który
dostrzegasz i cenisz.
    </li>
    <li>
    Pamiętaj o uczuciach – powiedz, co czujesz, jakie emocje Ci towarzyszą.
    </li>
    <li>
    Daj szansę wyrazić się odbiorcy – zadaj pytania otwarte.
    </li>
</ol>
</p>
