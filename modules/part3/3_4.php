<h2>Wiedza</h2>
<p>Na które zebranie chętniej byś się wybrał jako wychowawca albo rodzic? Z pewnością
pierwsze wymaga mniejszego przygotowania od nauczyciela. Oceny są wystawione, średnią
zliczy dziennik elektroniczny. Problemy najprawdopodobniej będą podobne do tych z lat
ubiegłych lub do tych z innych klas. Plan drugiego zebrania jest bardziej wymagający,
zwłaszcza dla rodziców: będą musieli powiedzieć, co z ich perspektywy było największym
wyzwaniem, będą musieli zastanowić się, co dobrego się wydarzyło, a nad czym warto dalej
pracować. Skąd mają to wiedzieć? Jeśli rozmawiali z dziećmi, nie będzie to stanowić kłopotu,
ale jeśli na pytanie „Jak było w szkole?” słyszeli odpowiedzi, że „dobrze”, „fajnie”…</p>
<p>
Dlatego warto zachęcać i dzieci, i rodziców do podsumowywania wydarzeń i procesów. Jako
nauczyciele jesteśmy dobrze przygotowani i przyzwyczajeni do omawiania działów, tematów,
sprawdzianów. Czas tak samo podejść do wycieczek, dni tematycznych, działań
wolontariackich, a czasem po prostu tygodnia czy nawet danego dnia.
</p>
<div>
<p>
Podsumowywanie to też ważna umiejętność potrzebna w pracy, nauce i budowaniu
samoświadomości. Pozwala:
</p>
<ul>
    <li>
    nazywać uczucia, emocje, nowo zdobytą wiedzę i umiejętności;
    </li>
    <li>
    oceniać stan postępu wiedzy, umiejętności, motywacji;
    </li>
    <li>
    dostrzegać efekty swojej pracy, wkładanego wysiłku;
    </li>
    <li>
    analizować przyczyny i skutki swoich zachowań, podjętych decyzji;
    </li>
    <li>
    doceniać nie tylko efekt, ale sam proces zmian;
    </li>
    <li>
    motywować do kolejnych działań dzięki świętowaniu odniesionych sukcesów lub
wyciągnięciu wniosków z popełnionych błędów.
    </li>
</ul>
</div>
<p>
Podsumowywanie jest wpisane w naturę człowieka. Każdy z nas dokonuje podsumowań -
większość co najmniej raz w roku, czego dowodem są postanowienia noworoczne. Dla
nastolatków podsumowania są równie ważne, chociaż często robią je nieświadomie,
intuicyjnie. Warto towarzyszyć im w tym procesie.
</p>
<p>
Świętowanie nawet niewielkich sukcesów powiązane jest z tą radosną i spontaniczną częścią
podsumowywania, dzięki któremu doświadczamy całej gamy pozytywnych uczuć. Viktor
Frankl w książce “Człowiek w poszukiwaniu sensu” napisał, że świętowanie nadaje sens
naszym wysiłkom, że dzięki niemu stajemy się silniejsi. Świętowanie to utożsamienie się z
odniesionym sukcesem, zaakceptowanie naszych osiągnięć, budowanie wewnętrznego
przekonania, że zasługujemy na to, co się wydarzyło. To także możliwość nie tylko
intelektualnego, ale też fizycznego odczucia, że naprawdę odnieśliśmy sukces. To kolejny
krok do budowania motywacji do podejmowania kolejnych wyzwań.
</p>
<p>
Podsumowania wiążą się często z udzielaniem i otrzymywaniem informacji zwrotnych.
Dotyczą one naszych zachowań, decyzji lub efektów pracy. Dla wielu młodych ludzi przyjęcie
krytycznej informacji zwrotnej jest trudne, rozwojowo bowiem na etapie adolescencji
skupieni są na sobie, czemu towarzyszy niestabilność emocjonalna, niskie poczucie własnej
wartości oraz dążenie do uzyskania “świętego spokoju”. Trudno w takich warunkach
przyjmować informację zwrotną. Z tego powodu warto uczyć uczniów i rodziców, jak
formułować informację zwrotną, czemu ma ona służyć i jak na nią reagować. O tym, jak to
robić, dowiesz się z podcastu oraz scenariusza zajęć.
</p>
