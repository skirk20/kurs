<h2>Wiedza</h2>
<p>Żyjemy w świecie przekazów mówiących nam, że można odnieść sukces bez pracy, że można
schudnąć bez ograniczenia kaloryczności jedzenia i zwiększenia dawki ruchu, że można
nauczyć się języka obcego bez uczenia się słówek i gramatyki. Jest to uproszczony komunikat.
Traktowany, niestety, przez wielu młodych ludzi jako prawda. W rzeczywistości, aby dojść do
wartościowych i dających satysfakcję efektów, trzeba najpierw wykonać jakieś działania, które najczęściej są
albo nieprzyjemne, albo trudne, albo wymagają wysiłku. Badania naukowe temu wtórują:
pokazują, że największy sukces odnoszą nie ci ludzie, którzy są najbardziej utalentowani, ale
ci, którzy wytrwale pracują nad osiągnięciem celu.</p>
<p>
Wytrwałość to efekt posiadania kilku umiejętności: wyznaczania celu i konsekwentnego
dążenia do niego bez zniechęcania się czy rezygnowania ze względu na napotkane trudności
oraz upływ czasu. Czasami do osiągnięcia celu potrzebne jest podejmowanie wielu prób
i sprawdzenie różnych możliwości działania.
</p>
<p>
Wytrwałość pozwala nam pokonać dyskomfort związany z oparciem się pokusie (chcę dostać
lepszą ocenę na sprawdzianie, więc będę się teraz uczyć, a dopiero potem pogram w piłkę
nożną), oderwaniem się od tego, co nas rozprasza (najpierw poukładam książki na półce,
a potem dopiero dokończę czytanie znalezionej na stercie książki) czy zignorowaniem
pomysłów na inne działania (w trakcie nauki do sprawdzianu z matematyki włączę komputer
i zajmę się ulubioną grą strategiczną).
</p>
<p>
Nie jest to umiejętność, z którą się rodzimy, ale możemy ją trenować – tak samo, jak
trenujemy inne umiejętności. Im szybciej zaczniemy, tym lepiej, bo tylko dzięki wierze,
że trening czyni mistrza bardziej angażujemy się w działania prowadzące nas do celu. Dzięki
temu zamiast unikać trudności mierzymy się z nimi i nie załamujemy się popełnionymi
błędami, ponieważ traktujemy je jako sytuację do wyciągnięcia wniosków na przyszłość.
</p>
<p>
Jakie działania dorosłych sprzyjają kształtowaniu postawy wytrwałości u nastolatków?
Przede wszystkim wytrwałość w działaniu nas samych – nasze dzieci słuchają tego, co do nich
mówimy, ale naśladują przede wszystkim to, co widzą. Jeśli sami nie jesteśmy wytrwali
w działaniach, to nie będziemy też wiarygodni jako osoby kształtujące tę postawę u innych.
</p>
<p>
Często popełnianym przez dorosłych błędem jest wyręczanie dzieci w wykonywaniu zadań
lub dokańczanie ich za nie w momencie, kiedy wykazują jakiekolwiek objawy zniechęcenia (np. prosisz, aby ustawili krzesła w kręgu, po czym, aby to się faktycznie wydarzyło,
aranżujesz przestrzeń samodzielnie). Aby rozwijać wytrwałość nie możemy usuwać każdej
przeszkody z drogi dzieci, tylko motywować, zachęcać do poszukiwania innego rozwiązania,
pokazywać, jak wiele już osiągnęli i kierować ich uwagę na cel, do którego dążą.
</p>
<p>
Dobrą praktyką jest ustalenie, za wykonywanie jakich obowiązków domowych
odpowiedzialne jest młode pokolenie. Jeśli od najmłodszych lat przyzwyczajamy je do
wykonywania rzeczy, które nie są atrakcyjne, ale są potrzebne, budujemy zdrowy fundament
na przyszłość. Wraz z wzrastającą odpowiedzialnością dziecka obowiązki mogą być coraz
bardziej rozbudowane, aczkolwiek zawsze muszą być dopasowane do możliwości naszej
latorośli. Pamiętajmy, że oprócz kształtowania postawy wytrwałości obowiązki domowe
pozytywnie wpływają na budowanie poczucia odpowiedzialności i sprawczości.
</p>
<p>
Niezależnie od tego, w jakim wieku jest dziecko, jeśli widzisz, że wytrwałość nie jest jego
mocną stroną, musisz zacząć od drobnych działań i prostych zadań, które dają się zrealizować
dość szybko. Początkowo za każdym razem proponuj dziecku swoją pomoc i doceniaj, że
wykonało zadanie do końca (nawet, jeśli nie zrobiło tego od razu idealnie). Pomagaj tak
mało, na ile to tylko możliwe, i nie wyręczaj, ale okazuj zainteresowanie i motywuj do
zakończenia działania.
</p>
<p>
Ustalając cel do realizacji podziel go na krótkie etapy/działania, których zakończenie łatwo
jest zauważyć. Np. jeśli celem jest sprzątnięty pokój, to omówcie, jakie działania/zadania
wchodzą w zakres tego celu: np
</p>
