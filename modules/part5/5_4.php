<p>
Dalszym etapem kształtowania tej postawy jest skoncentrowanie się na celu bardziej
odległym w czasie, ale ważnym i atrakcyjnym dla dziecka. W szkole to może być np. zdobycie
czerwonego paska na koniec roku, organizacja dyskoteki, kiermaszu czy imprezy sportowej, a w domu np. odłożenie pewnej kwoty z kieszonkowego na wymarzony zakup lub regularne
zajmowanie się hobby przez kilka miesięcy. W trakcie osiągania celu rozmawiaj o nim
z dzieckiem, zachęcaj do działania, ale za każdym razem udzielaj mu tylko tyle wskazówek
i pomocy, ile minimalnie potrzeba, aby odniosło sukces.
</p>
<p>
Dawaj informacje zwrotne na temat postępów w drodze do celu. Mogą być one werbalne
lub, w przypadku dzieci łatwo się dekoncentrujących i zapominających o tym, co mają zrobić,
może to być zaznaczenie konkretnego osiągnięcia (punktu) na stworzonej przez was mapie
prowadzącej do celu. Warto, aby taka mapa była w miejscu łatwo dostępnym, aby młody
człowiek mógł często sprawdzać, co już osiągnął, a co jeszcze przed nim – na drzwiach
lodówki, na ścianie w pokoju, przyklejona w notesie ucznia.
</p>
<p>
Przypominaj nastolatkowi o tym, jaki jest cel. Jeśli np. zbiera pieniądze na określoną rzecz –
umieść zdjęcie tej rzeczy w łatwo dostępnym dla niego miejscu. Takie zdjęcie będzie dużo
bardziej skuteczne niż werbalne przypominanie o celu – nastolatki często traktują nasze
słowa jako zrzędzenie, marudzenie czy przejaw niewiary w ich odpowiedzialność.
</p>
<p>
Ustal z uczniem/dzieckiem, jaka forma przypomnień o terminach/zadaniach/ ustaleniach jest
dla niego najlepsza – czy to przypomnienie wyświetlające się co określony czas w jej/jego
telefonie, czy może wygaszacz ekranu, który będzie przypominajką? A może minutnik, który
będzie przypominał o tym, że przerwa w nauce miała trwać 30 minut, a nie dłużej? Ustalenie
takich form „mechanicznych” przypomnień jest bardzo korzystnym rozwiązaniem, bo nie
powoduje u dziecka poczucia, że to dorosły mu coś narzuca czy ogranicza przyjemności, a
więc nie prowokuje konfliktów, o które łatwo przy niestabilnych emocjach nastolatka.
</p>

