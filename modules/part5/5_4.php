<h2>Wiedza</h2>
<p>Żyjemy w świecie przekazów mówiących nam, że&nbspmożna odnieść sukces bez pracy, że&nbspmożna
schudnąć bez ograniczenia kaloryczności jedzenia i&nbspzwiększenia dawki ruchu, że&nbspmożna
nauczyć się języka obcego bez uczenia się słówek i&nbspgramatyki. Jest to uproszczony komunikat.
Traktowany, niestety, przez wielu młodych ludzi jako prawda. W&nbsprzeczywistości, aby dojść do&nbspwartościowych i&nbspdających satysfakcję efektów, trzeba najpierw wykonać jakieś działania, które najczęściej są
albo nieprzyjemne, albo trudne, albo wymagają wysiłku. Badania naukowe temu wtórują:
pokazują, że największy sukces odnoszą nie ci ludzie, którzy są najbardziej utalentowani, ale
ci, którzy wytrwale pracują nad osiągnięciem celu.</p>
<p>
Wytrwałość to efekt posiadania kilku umiejętności: wyznaczania celu i&nbspkonsekwentnego
dążenia do&nbspniego bez zniechęcania się czy rezygnowania ze&nbspwzględu na napotkane trudności
oraz upływ czasu. Czasami do osiągnięcia celu potrzebne jest podejmowanie wielu prób
i&nbspsprawdzenie różnych możliwości działania.
</p>
<p>
Wytrwałość pozwala nam pokonać dyskomfort związany z&nbspoparciem się pokusie (chcę dostać
lepszą ocenę na sprawdzianie, więc będę się teraz uczyć, a&nbspdopiero potem pogram w&nbsppiłkę
nożną), oderwaniem się od&nbsptego, co nas rozprasza (najpierw poukładam książki na półce,
a&nbsppotem dopiero dokończę czytanie znalezionej na stercie książki) czy zignorowaniem
pomysłów na inne działania (w trakcie nauki do sprawdzianu z&nbspmatematyki włączę komputer
i&nbspzajmę się ulubioną grą strategiczną).
</p>
<p>
Nie jest to umiejętność, z&nbspktórą się rodzimy, ale możemy ją trenować – tak samo, jak
trenujemy inne umiejętności. Im szybciej zaczniemy, tym lepiej, bo tylko dzięki wierze,
że&nbsptrening czyni mistrza bardziej angażujemy się w&nbspdziałania prowadzące nas do celu. Dzięki
temu zamiast unikać trudności mierzymy się z nimi i&nbspnie załamujemy się popełnionymi
błędami, ponieważ traktujemy je jako sytuację do&nbspwyciągnięcia wniosków na przyszłość.
</p>
<p>
Jakie działania dorosłych sprzyjają kształtowaniu postawy wytrwałości u&nbspnastolatków?
Przede wszystkim wytrwałość w&nbspdziałaniu nas samych – nasze dzieci słuchają tego, co do&nbspnich
mówimy, ale naśladują przede wszystkim to, co widzą. Jeśli sami nie jesteśmy wytrwali
w&nbspdziałaniach, to nie będziemy też wiarygodni jako osoby kształtujące tę postawę u&nbspinnych.
</p>
<p>
Często popełnianym przez dorosłych błędem jest wyręczanie dzieci w&nbspwykonywaniu zadań
lub dokańczanie ich za nie w&nbspmomencie, kiedy wykazują jakiekolwiek objawy zniechęcenia (np. prosisz, aby ustawili krzesła w&nbspkręgu, po czym, aby to się faktycznie wydarzyło,
aranżujesz przestrzeń samodzielnie). Aby rozwijać wytrwałość nie możemy usuwać każdej
przeszkody z&nbspdrogi dzieci, tylko motywować, zachęcać do&nbspposzukiwania innego rozwiązania,
pokazywać, jak wiele już osiągnęli i&nbspkierować ich uwagę na cel, do&nbspktórego dążą.
</p>
<p>
Dobrą praktyką jest ustalenie, za&nbspwykonywanie jakich obowiązków domowych
odpowiedzialne jest młode pokolenie. Jeśli od&nbspnajmłodszych lat przyzwyczajamy je do&nbspwykonywania rzeczy, które nie są atrakcyjne, ale są potrzebne, budujemy zdrowy fundament
na przyszłość. Wraz z&nbspwzrastającą odpowiedzialnością dziecka obowiązki mogą być coraz
bardziej rozbudowane, aczkolwiek zawsze muszą być dopasowane do&nbspmożliwości naszej
latorośli. Pamiętajmy, że&nbspoprócz kształtowania postawy wytrwałości obowiązki domowe
pozytywnie wpływają na budowanie poczucia odpowiedzialności i&nbspsprawczości.
</p>
<p>
Niezależnie od tego, w&nbspjakim wieku jest dziecko, jeśli widzisz, że&nbspwytrwałość nie jest jego
mocną stroną, musisz zacząć od&nbspdrobnych działań i&nbspprostych zadań, które dają się zrealizować
dość szybko. Początkowo za&nbspkażdym razem proponuj dziecku swoją pomoc i&nbspdoceniaj, że
wykonało zadanie do&nbspkońca (nawet, jeśli nie zrobiło tego od&nbsprazu idealnie). Pomagaj tak
mało, na ile to tylko możliwe, i&nbspnie wyręczaj, ale okazuj zainteresowanie i&nbspmotywuj do
zakończenia działania.
</p>
