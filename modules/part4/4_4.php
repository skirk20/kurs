<h2>Wiedza</h2>
<p>Początek dorastania to jeden z najtrudniejszych momentów w życiu człowieka. Doświadcza
on wtedy kryzysu tożsamości, bo przestał już być małym dzieckiem, a jeszcze nie jest
dorosłym. Staje przed wyzwaniem odpowiedzenia na pytania kim jest teraz i kim chce być w
przyszłości. Nakładają się na to oczekiwania rodziców, nauczycieli i systemu edukacji. Młody
człowiek nie wie, jak ma funkcjonować, bo schematy wypracowane w dzieciństwie okazują
się nieskuteczne. Mówi się, że nastolatek jest rozproszony. Poczucie rozproszenia można
opisać jako stan dezintegracji, jako odczuwanie siebie w sposób niestabilny (w tych samych
sytuacjach zachowuje się teraz w zupełnie odmienny, nieprzewidywalny sposób) i niejasny
(nie umie nazwać tego, co czuje ani analizować tego, jak myśli). Ten stan zawieszenia między
dzieciństwem a dorosłością jest odczuwany zarówno przez samego nastolatka, jak i przez
osoby z jego otoczenia 8 .</p>
<p>
Młody człowiek często przyjmuje strategię unikania – dąży do świętego spokoju, nie chce
podejmować żadnych decyzji, bo nie wie, czym się kierować (nie wyznaje stabilnego systemu
wartości), czuje, że nie ma odpowiednich kompetencji (chociaż tego nie powie).
W konsekwencji stara się działać tu i teraz, a nie planować w dłuższej perspektywie. To
wszystko sprawia, że wielu młodych ludzi ma wielką trudność z określeniem swoich planów,
celów a nawet marzeń.
</p>
<p>
Bez planowania trudno mówić o motywacji (skoro nie wiadomo, co jest moim celem, to
trudno zmotywować się do jakiegokolwiek wysiłku) i trudno marzyć o podejmowaniu decyzji
bardziej wartościowych niż te, które dają przyjemność tu i teraz. Bez planowania też trudno
mieć poczucie satysfakcji z osiągniętego celu, bo nie wiadomo, co tym celem miałoby być.
Planowanie oznacza wyznaczenie kierunku działania - w krótszej lub dłuższej perspektywie -
oraz stworzenie mapy, która pozwoli nie zgubić drogi do obranego celu. Chodzi o zbudowanie nawyku wybiegania myślami poza dzień dzisiejszy, myślenie o tym, jaki chcę
być i kim chcę być za rok, dwa, pięć czy dziesięć lat. Kiedy mamy wyobrażenie tego, co
chcemy osiągnąć, prościej nam podejmować działania zbliżające nas do tego celu.
</p>
<p>
Ze względu na ograniczoną możliwość przewidywania konsekwencji wielu nastolatków nie
jest w stanie samodzielnie wyznaczać sobie celów rozwojowych i potrzebuje nas, dorosłych,
do pomocy. Jak możemy wspierać w tym rodziców? Przede wszystkim podsuwając
wskazówki, jak rozmawiać z młodymi ludźmi, aby samodzielnie znaleźli odpowiedź na
pytanie kim są i czego chcą. Pomysły, jak to robić, znajdziesz w scenariuszu aktywności.
</p>
