<p>
Dawaj przykład jako dyrektor i&nbspprzywódca organizacji. Jesteś postacią
odgrywającą w&nbspzespole dużą rolę, więc to, o&nbspczym i&nbspw&nbspjaki sposób mówisz, oddziałuje na całą
