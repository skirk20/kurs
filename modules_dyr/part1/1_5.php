    <h2>Praktyka</h2>
    <h3>scenariusz rady pedagogicznej</h3>
    <p>
    Temat: Edukacja to relacja. Relacja to komunikacja.
    </p>
    <p>
    Czas: 120 minut (+ ewentualne rozszerzenie o dodatkowe 90 min.)
    </p>
    <p>
    Cel: Przypomnienie nauczycielom, jakie są funkcje szkoły. Zwiększenie wśród nauczycieli
świadomości tego, jakie postawy dotyczące wychowywania młodzieży są im bliskie.
Poszukanie odpowiedzi na pytania, jakie cechy powinien mieć wychowawca klasy.
Uświadomienie nauczycielom, jakie działania po stronie szkoły przekładają się na większą
efektywność dydaktyczno-wychowawczą.
    </p>
    
