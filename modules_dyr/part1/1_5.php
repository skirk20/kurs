<h3>uwagi organizacyjne</h3>
    <p>
        
        Zaproś kadrę pedagogiczną do zajęcia miejsc w&nbspkręgu - ważne, aby także przestrzeń
sygnalizowała, że wszyscy jesteście tak samo ważni i&nbsppotrzebni, bo jesteście jednym
zespołem. Zadawaj dużo pytań otwartych, zachęcaj do&nbspgenerowania pomysłów, nie narzucaj
swojego zdania, słuchaj, upewniaj się, czy dobrze rozumiesz, dbaj o&nbspto, aby sformułowania
były dla wszystkich jasne i&nbspjak najbardziej konkretne.
    </p>
        
        <ol>
            <li>Faza wstępna [20 min]</li>
            <ul>
            <li>
            Przywitaj się z&nbspwychowawcami. Wewnątrz kręgu rozłóż cytaty poświęcone edukacji
(załącznik nr 1). Poproś, aby każdy nauczycieli wybrał jedno, które jest mu bliskie. Ty
też to zrób. Następnie odczytajcie cytaty i&nbspuzasadnijcie swój wybór.
            </li>
            <li>
            Wyjaśnij cel spotkania nawiązując do wykonanego ćwiczenia.
            </li></ul>
        <li>
        Faza realizacyjna [120 min]
        </li>
        <ul>
        <li>
        Podziel nauczycieli na kilkuosobowe grupy (najlepiej zastosuj dobór losowy). Poproś,
aby zastanowili się, po co jest wychowawca, jaką ma rolę i&nbspjakie są jego zadania z&nbsptym
związane. Swoje wnioski nauczyciele zapisują na samoprzylepnych karteczkach lub na
flipcharcie. W&nbsptym ćwiczeniu nauczyciele powinni przede wszystkim odwołać się do
swojej misji, tożsamości i&nbspprzekonań, równie istotna będzie praktyka.
        </li>
        <li>
        Zachęć nauczycieli do przedstawienia efektów swojej pracy. Twoją rolą powinno być
zadbanie o&nbspjasność przekazu, upewnienie się, czy dobrze rozumiesz wychowawców:
zadawaj pytania służące klaryfikacji i&nbspdoprecyzowaniu, np. czy dobrze
zrozumiałem/am, że? Jak mam to rozumieć? Czy możesz powiedzieć więcej na ten
temat?
        </li>
        <li>
        Podsumuj ćwiczenie zwracając uwagę na konieczność budowania kompetencji
miękkich uczniów oraz wzmacnianie relacji. Nauczyciele powinni usłyszeć od Ciebie
jako lidera organizacji, że na efektywność edukacyjną bardzo duży wpływ mają relację
z&nbspnauczycielem. Nie jest to tylko hasło, ale wniosek z&nbspbadań prof. Hattiego. Nawiąż
również do prawa oświatowego - wskaż zadania nauczycieli w&nbspkontekście działań
wychowawczych. Zrób to w&nbspszczególności, jeśli nauczyciele będą podważać
konieczność realizacji tego rodzaju działań. Możesz skorzystać z&nbspmateriałów z&nbspZałącznika nr 2.
        </li>
        <li>
        Nauczyciele są już świadomi roli i&nbspkonieczności prowadzenia działań wychowawczych.
Czas, aby zastanowili się, jak powinny wyglądać godziny wychowawcze oraz zebrania
z&nbsprodzicami. Poproś ich, aby w grupach (najlepiej w podziale według poziomu
oddziałów, których są wychowawcami) udzielili odpowiedzi na pytania zawarte
w&nbspPrzemyśleniach (załącznik nr 3).
        </li>
        <li>
        Nauczyciele prezentują wyniki swojej pracy. Zacznij rozmowę na ten temat. Twoją
rolą powinno być zadbanie o&nbspjasność przekazu, upewnienie się, czy dobrze rozumiesz
wychowawców. Masz prawo dodawać komentarze istotne z&nbspTwojego punktu
widzenia.
        </li>
        <li>
        Jako lider organizacji poproś, aby nauczyciele zastanowili się, czego potrzebują, aby
takie godziny wychowawcze i&nbspzebrania prowadzić. Możesz tę aktywność poprowadzić na forum rady lub jako pracę w&nbspgrupach. Zbierz informację zwrotną, porozmawiajcie
o&nbsptym.
        </li>
        <li>
        Podsumuj stwierdzając, że celem jest zwrócenie większej uwagi na budowanie relacji
z&nbspuczniami i&nbsprodzicami, głównie poprzez rozwijanie ich kompetencji społecznych.
Zachęć nauczycieli (lub wprost wskaż to jako obowiązek) do zaplanowania aktywności
w&nbspramach zajęć z&nbspwychowawcą oraz zebrań z&nbsprodzicami. W&nbsptym celu nauczyciele
mogą skorzystać z&nbsppakietu materiałów “Kurs na relacje”.
        </li>
        <li>
        Zwróć uwagę, że doskonalenie umiejętności budowania relacji wymaga od każdego
namysłu nad sobą i&nbspswoim rozwojem. Nauczyciele mają pod tym względem z&nbspjednej
strony wachlarz możliwości pracy w&nbspinterakcji z&nbspinnymi (kursy, szkolenia, współpraca
w&nbspradzie pedagogicznej, awans zawodowy), a&nbspz&nbspdrugiej strony ich zawód polega na
samodzielnej pracy, przez co łatwo można poczuć się osamotnionym i&nbspzniechęconym
do podejmowania dodatkowych działań. W&nbspzwiązku z&nbsptym zachęcasz do współpracy,
ale również do systematycznej autorefleksji. W&nbsptym celu omów “model cebuli” Freda
Korthagena (więcej informacji znajdziesz w&nbspmodule: wiedza).
        </li>
        <li>
        Nauczyciele samodzielnie wypełniają pytania do autorefleksji (załącznik nr 3).
Poinformuj ich, że będą one stanowiły podstawę do sprawowania nadzoru
pedagogicznego w&nbspformie wspomagania i&nbspkontroli. Jeżeli w radzie panuje atmosfera
rzeczywistej współpracy i&nbspbezpieczeństwa, to możesz poprosić nauczycieli
o&nbspomówienie w&nbspparach lub na forum swoich odpowiedzi.
        </li>
        </ul>
        <li>Faza podsumowująca [10 min]</li>
        <ul>
        <li>
        Usiądźcie w kręgu. Poproś, aby każdy nauczyciel powiedział, co dał mu ten warsztat.
        </li>
        <li>
        Na koniec zadeklaruj swoje wsparcie dla nauczycieli w&nbspbudowaniu relacji z uczniami
i&nbsprodzicami.
        </li>
        </ul>
</ol>
    
