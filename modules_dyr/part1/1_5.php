    <p>
        <h3>uwagi organizacyjne</h3>
        Zaproś kadrę pedagogiczną do zajęcia miejsc w kręgu - ważne, aby także przestrzeń
sygnalizowała, że wszyscy jesteście tak samo ważni i potrzebni, bo jesteście jednym
zespołem. Zadawaj dużo pytań otwartych, zachęcaj do generowania pomysłów, nie narzucaj
swojego zdania, słuchaj, upewniaj się, czy dobrze rozumiesz, dbaj o to, aby sformułowania
były dla wszystkich jasne i jak najbardziej konkretne.
    </p>
        
        <dl>
            <dt>Faza wstępna [20 min]</dt>
            <dd>
            Przywitaj się z wychowawcami. Wewnątrz kręgu rozłóż cytaty poświęcone edukacji
(załącznik nr 1). Poproś, aby każdy nauczycieli wybrał jedno, które jest mu bliskie. Ty
też to zrób. Następnie odczytajcie cytaty i uzasadnijcie swój wybór.
            </dd>
            <dd>
            Wyjaśnij cel spotkania nawiązując do wykonanego ćwiczenia.
            </dd>
        <dt>
        Faza realizacyjna [120 min]
        </dt>
        <dd>
        Podziel nauczycieli na kilkuosobowe grupy (najlepiej zastosuj dobór losowy). Poproś,
aby zastanowili się, po co jest wychowawca, jaką ma rolę i jakie są jego zadania z tym
związane. Swoje wnioski nauczyciele zapisują na samoprzylepnych karteczkach lub na
flipcharcie. W tym ćwiczeniu nauczyciele powinni przede wszystkim odwołać się do
swojej misji, tożsamości i przekonań, równie istotna będzie praktyka.
        </dd>
        <dd>
        Zachęć nauczycieli do przedstawienia efektów swojej pracy. Twoją rolą powinno być
zadbanie o jasność przekazu, upewnienie się, czy dobrze rozumiesz wychowawców:
zadawaj pytania służące klaryfikacji i doprecyzowaniu, np. czy dobrze
zrozumiałem/am, że? Jak mam to rozumieć? Czy możesz powiedzieć więcej na ten
temat?
        </dd>
        <dd>
        Podsumuj ćwiczenie zwracając uwagę na konieczność budowania kompetencji
miękkich uczniów oraz wzmacnianie relacji. Nauczyciele powinni usłyszeć od Ciebie
jako lidera organizacji, że na efektywność edukacyjną bardzo duży wpływ mają relację
z nauczycielem. Nie jest to tylko hasło, ale wniosek z badań prof. Hattiego. Nawiąż
również do prawa oświatowego - wskaż zadania nauczycieli w kontekście działań
wychowawczych. Zrób to w szczególności, jeśli nauczyciele będą podważać
konieczność realizacji tego rodzaju działań. Możesz skorzystać z materiałów z
Załącznika nr 2.
        </dd>
        <dd>
        Nauczyciele są już świadomi roli i konieczności prowadzenia działań wychowawczych.
Czas, aby zastanowili się, jak powinny wyglądać godziny wychowawcze oraz zebrania
z rodzicami. Poproś ich, aby w grupach (najlepiej w podziale według poziomu
oddziałów, których są wychowawcami) udzielili odpowiedzi na pytania zawarte
w Przemyśleniach (załącznik nr 3).
        </dd>
        <dd>
        Nauczyciele prezentują wyniki swojej pracy. Zacznij rozmowę na ten temat. Twoją
rolą powinno być zadbanie o jasność przekazu, upewnienie się, czy dobrze rozumiesz
wychowawców. Masz prawo dodawać komentarze istotne z Twojego punktu
widzenia.
        </dd>
        <dd>
        Jako lider organizacji poproś, aby nauczyciele zastanowili się, czego potrzebują, aby
takie godziny wychowawcze i zebrania prowadzić. Możesz tę aktywność poprowadzić na forum rady lub jako pracę w grupach. Zbierz informację zwrotną, porozmawiajcie
o tym.
        </dd>
        <dd>
        Podsumuj stwierdzając, że celem jest zwrócenie większej uwagi na budowanie relacji
z uczniami i rodzicami, głównie poprzez rozwijanie ich kompetencji społecznych.
Zachęć nauczycieli (lub wprost wskaż to jako obowiązek) do zaplanowania aktywności
w ramach zajęć z wychowawcą oraz zebrań z rodzicami. W tym celu nauczyciele
mogą skorzystać z pakietu materiałów “Kurs na relacje”.
        </dd>
        <dd>
        Zwróć uwagę, że doskonalenie umiejętności budowania relacji wymaga od każdego
namysłu nad sobą i swoim rozwojem. Nauczyciele mają pod tym względem z jednej
strony wachlarz możliwości pracy w interakcji z innymi (kursy, szkolenia, współpraca
w radzie pedagogicznej, awans zawodowy), a z drugiej strony ich zawód polega na
samodzielnej pracy, przez co łatwo można poczuć się osamotnionym i zniechęconym
do podejmowania dodatkowych działań. W związku z tym zachęcasz do współpracy,
ale również do systematycznej autorefleksji. W tym celu omów “model cebuli” Freda
Korthagena (więcej informacji znajdziesz w module: wiedza).
        </dd>
        <dd>
        Nauczyciele samodzielnie wypełniają pytania do autorefleksji (załącznik nr 3).
Poinformuj ich, że będą one stanowiły podstawę do sprawowania nadzoru
pedagogicznego w formie wspomagania i kontroli. Jeżeli w radzie panuje atmosfera
rzeczywistej współpracy i bezpieczeństwa, to możesz poprosić nauczycieli
o omówienie w parach lub na forum swoich odpowiedzi.
        </dd>
        <dt>Faza podsumowująca [10 min]</dt>
        <dd>
        Usiądźcie w kręgu. Poproś, aby każdy nauczyciel powiedział, co dał mu ten warsztat.
        </dd>
        <dd>
        Na koniec zadeklaruj swoje wsparcie dla nauczycieli w budowaniu relacji z uczniami
i rodzicami.
        </dd>
</dl>
    
