programu nauczania, na środowisko (czyli jedną z&nbspwarstw cebuli decydujących o&nbsptym, jaki jest
nauczyciel). Przyjrzyj się sobie, poproś innych, żeby Ci w&nbsptym pomogli. Dokonaj autorefleksji.
