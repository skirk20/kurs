Zainicjuj zmianę ukrytego programu szkoły
</li>
</ol>
<p>
Ukryty program szkoły to określenie wszystkiego, czego uczy szkoła, a co nie jest jawnie
nazwane. Są to m.in. nieuświadomione lub nienazwane wartości, zachowania czy stereotypy,
często stojące w sprzeczności z oficjalnie deklarowanymi. Szkoła może mieć wpisane
w statucie czy programie profilaktyczno-wychowawczym dbanie o podmiotowość uczniów,
jednak dla nauczycieli i uczniów może być oczywiste, że to puste deklaracje: bo np. w
sekretariacie jest niemiła pani, która nieprzyjemnie informuje, że dyrektor przyjmuje
petentów w określonych godzinach i żeby uczeń zastanowił się, czy naprawdę ma się coś
ważnego do załatwienia, a wszystkie zgłoszone pomysły uczniów z zupełnie nieznanego
powodu nigdy nie mogą zostać zrealizowane (nie da się). Na stronie szkoły może być
napisane, że istotne jest uczenie różnorodnymi metodami, ale kiedy nauczyciel chce
zorganizować pracę twórczą z wykorzystaniem materiałów plastycznych, to słyszy
informację, że jest to niemożliwe, bo potem powstaje duży bałagan i panie sprzątaczki się na
to nie zgadzają. Po otrzymaniu reprymendy, że w sali po lekcji polskiego znowu był
nieporządek, polonista następnym razem dwa razy zastanowi się, czy robić coś innego niż
wykład i notatka.
</p>
<p>
Warto przyjrzeć się wspólnie z pracownikami szkoły, uczniami i rodzicami, co stanowi wasz
ukryty program (każda szkoła go ma). Dzięki temu będziecie mogli przyjrzeć się, jakie
komunikaty wysyłacie i na ile są one spójne albo sprzeczne z tym, co byście chcieli
kształtować w waszych uczniach, rodzicach i pracownikach. Dopiero świadomość, jaki jest
ukryty program szkoły, niweluje ryzyko, że działa on wbrew Waszej misji i wizji.
</p>
<ul>
<li>
Modeluj
</li>
</ul>
<p>
Dawaj przykład jako dyrektor i przywódca organizacji dawaj przykład. Jesteś postacią
odgrywającą w zespole dużą rolę, więc to, o czym i w jaki sposób mówisz, oddziałuje na cała
społeczność. Zwróć szczególną uwagę na to, co komunikujesz jako ważne dla Ciebie, na co
kładziesz nacisk. Twoje działania mają szczególnie duży wpływ na kształtowanie ukrytego
programu nauczania, na środowisko (czyli jedną z warstw cebuli decydujących o tym, jaki jest
nauczyciel). Przyjrzyj się sobie, poproś innych, żeby Ci w tym pomogli. Dokonaj autorefleksji.
Jakie są Twoje wartości? Co myślisz o sobie jako dyrektorze? Jak odbierają Cię inni? Co cenisz
w swojej szkole, a co chciałbyś w niej zmienić i dlaczego? Zastanów się, co chcesz
komunikować zespołowi. Zacznij to robić w sposób uświadomiony i zaplanowany.
</p>
<ul>
<li>
    Wspieraj
</li>
</ul>
<p>
Nie zmuszaj do objęcia wychowawstwa, jeśli zależy Ci na jego jakości. Nie przydzielaj
wychowawstwa wbrew woli nauczyciela, porozmawiaj o tym, jakie potrzeby stoją za tym,
aby to właśnie ona/on został wychowawcą tej klasy, dlaczego uważasz, że to właśnie ona/on
jest odpowiednia osobą w tej roli.
</p>
<p>
Zaproponuj objęcie mentoringiem. Nauczycielom, którzy są nowi w twoim zespole,
zaproponuj współpracę z osobą, która będzie wsparciem w poznawaniu kultury organizacji
Twojej szkoły. Może to być opiekun stażu, ale może to też być zupełnie inna osoba, która
chętnie dzieli się swoim doświadczeniem i którą Ty sam cenisz jako wychowawcę czy
nauczyciela.
</p>
<p>
Zaproponuj wsparcie zewnętrzne. Być może bezpłatny kurs dla początkujących
wychowawców organizuje lokalny ośrodek doskonalenia nauczycieli? A może polecisz
internetowe grupy wsparcia dla początkujących nauczycieli? A może w budżecie szkoły
znajdą się finanse na opłacenie kursu, warsztatów albo wręcz szkolenie dla kadry
pedagogicznej? Nawet jeśli budżet szkoły nie pozwala zrealizować wymarzonego
scenariusza, to zawsze można podsunąć nowemu nauczycielowi wartościową literaturę,
podcasty do odsłuchania lub inspirujące materiały w internecie.
</p>
<p>
Nie krytykuj za popełnione błędy. Stosuj informację zwrotną lub inne formy komunikowania
tego, co zaobserwowałeś/co Cię niepokoi, jak najczęściej stosuj komunikat JA i nie uogólniaj,
Pamiętaj, że dopiero nauczyciel, który czuje się bezpieczny i akceptowany, będzie gotów do
wyjścia ze swojej strefy komfortu i wykorzystania nowych rozwiązań czy metod pracy.
</p>
