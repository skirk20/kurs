<p>
Ukryty program szkoły to określenie wszystkiego, czego uczy szkoła, a&nbspco nie jest jawnie
nazwane. Są to m.in. nieuświadomione lub nienazwane wartości, zachowania czy stereotypy,
często stojące w sprzeczności z&nbspoficjalnie deklarowanymi. Szkoła może mieć wpisane
w&nbspstatucie czy programie profilaktyczno-wychowawczym dbanie o&nbsppodmiotowość uczniów,
jednak dla nauczycieli i&nbspuczniów może być oczywiste, że to puste deklaracje: bo&nbspnp. w
sekretariacie jest niemiła pani, która nieprzyjemnie informuje, że dyrektor przyjmuje
petentów w określonych godzinach i&nbspżeby uczeń zastanowił się, czy naprawdę ma się coś
ważnego do&nbspzałatwienia, a&nbspwszystkie zgłoszone pomysły uczniów z&nbspzupełnie nieznanego
powodu nigdy nie mogą zostać zrealizowane (nie da się). Na stronie szkoły może być
napisane, że istotne jest uczenie różnorodnymi metodami, ale kiedy nauczyciel chce
zorganizować pracę twórczą z&nbspwykorzystaniem materiałów plastycznych, to słyszy
informację, że jest to niemożliwe, bo potem powstaje duży bałagan i&nbsppanie sprzątaczki się na
to nie zgadzają. Po otrzymaniu reprymendy, że w&nbspsali po lekcji polskiego znowu był
nieporządek, polonista następnym razem dwa razy zastanowi się, czy robić coś innego niż
wykład i&nbspnotatka.
</p>
<p>
Warto przyjrzeć się wspólnie z pracownikami szkoły, uczniami i&nbsprodzicami, co stanowi wasz
ukryty program (każda szkoła go ma). Dzięki temu będziecie mogli przyjrzeć się, jakie
komunikaty wysyłacie i&nbspna ile są one spójne albo sprzeczne z&nbsptym, co byście chcieli
kształtować w&nbspwaszych uczniach, rodzicach i&nbsppracownikach. Dopiero świadomość, jaki jest
ukryty program szkoły, niweluje ryzyko, że działa on wbrew Waszej misji i&nbspwizji.
</p>
