<h2>Wiedza</h2>
<p>Już w pierwszym artykule ustawy Prawo oświatowe czytamy, że “system oświaty zapewnia
w szczególności: 1) realizację prawa każdego obywatela Rzeczypospolitej Polskiej do
kształcenia się oraz prawa dzieci i młodzieży do wychowania i opieki, odpowiednich do wieku
i osiągniętego rozwoju” a definicja wychowania to “wspieranie dziecka w rozwoju ku pełnej
dojrzałości w sferze fizycznej, emocjonalnej, intelektualnej, duchowej i społecznej,
wzmacniane i uzupełniane przez działania z zakresu profilaktyki problemów dzieci
i młodzieży.”</p>
    <p>
    Wynika z tego zapisu jasno, że zadaniem nauczycieli jest nie tylko dbanie o rozwój
poznawczy ucznia, ale także oddziaływania wychowawcze wobec młodych ludzi.
    </p>
    <p>
    Agnieszkę i Magdę zobowiązuje do tego również ustawa Karta Nauczyciela:
    </p>
    <p>
    Nauczyciel zobowiązany jest:
    </p>
    <ol>
        
        <li>rzetelnie realizować zadania związane z powierzonym mu stanowiskiem oraz
podstawowymi funkcjami szkoły: dydaktyczną, wychowawczą i opiekuńczą, w tym
zadania związane z zapewnieniem bezpieczeństwa uczniom w czasie zajęć
organizowanych przez szkołę;</li>
<li>wspierać każdego ucznia w jego rozwoju;</li>
<li>...</li>
<li>kształcić i wychowywać młodzież w umiłowaniu Ojczyzny, w poszanowaniu
Konstytucji Rzeczypospolitej Polskiej, w atmosferze wolności sumienia i szacunku dla
każdego człowieka;</li>
<li>dbać o kształtowanie u uczniów postaw moralnych i obywatelskich zgodnie z ideą
demokracji, pokoju i przyjaźni między ludźmi różnych narodów, ras i światopoglądów.</li>
    </ol>
    <p>
    Agnieszka organizując dodatkową lekcję języka polskiego skupia się na funkcji dydaktycznej
szkoły. Przekazując specjalistom sprawy uczniów niejako zdejmuje z siebie
odpowiedzialność za równie ważną funkcję wychowawczą. Daje młodym ludziom szansę na
zdobycie kompetencji polonistycznych i jednocześnie ogranicza możliwość rozwoju
psychospołecznego.
    </p>
    <p>
    Ograniczanie działań wychowawczych na rzecz dydaktycznych jest w polskich szkołach
praktyką coraz powszechniejszą. Z jednej strony jest to spowodowane brakiem
przygotowania do pełnienia roli wychowawczej, z drugiej - często brakiem wsparcia dla
nauczycieli, którzy zaczynają pełnić tę rolę. Prowadzi to z jednej strony do obniżenia
kompetencji wychowawczych nauczycieli i spadku wiary w znaczenie ich roli wychowawczej,
a z drugiej pozbawia uczniów, szczególnie tych z dysfunkcyjnych lub niewspierających rodzin,
dostępu do możliwości nabycia umiejętności potrzebnych przez całe życie.
    </p>
    <p>
    Pojawia się zamknięte koło: wstrzymywanie się od prowadzenia realnych działań
wychowawczych sprawia, że nauczyciele nie mają okazji do odniesienia sukcesów w tym
obszarze i wyciągania wniosków z popełnionych błędów, w konsekwencji tracą wiarę we
własne możliwości wsparcia emocjonalnego uczniów i rozwijania ich kompetencji
społecznych, w związku z czym przekierowują swoją aktywność wyłącznie na funkcje
dydaktyczne. Skutkuje to obniżeniem autorytetu nauczycieli jako specjalistów w zakresie
rozwoju młodego człowieka.
    </p>
    <p>
    Nawet jeśli dla Ciebie, dyrektora szkoły, najważniejsze są wyniki dydaktyczne, to warto wziąć
pod uwagę wnioski płynące z badań i metaanaliz dotyczących czynników wpływających na
wyniki w nauce zrealizowane przez prof. Johna Hattiego. Wykazał on, że dla procesu uczenia
się istotne znaczenie ma relacja z nauczycielem oraz współpraca nauczyciela z rodzicami,
oparcie się na strategiach i metodach wymagających dialogu oraz zwrócenie uwagi na proces zdobywania wiedzy i umiejętności, a nie wyłącznie wynik. Zachęcamy Cię do
przeanalizowania wyników badań z załącznika nr 1 oraz zapoznania z tymi badaniami kadry
pedagogicznej Twojej szkoły.
    </p>
<p>
Niezależnie od tego, czy rzecz dotyczy młodzieży, czy dorosłych, naszych uczniów czy naszych
nauczycieli, to do skutecznego działania niezbędna jest motywacja wewnętrzna. Aby mogła
być ona siłą napędową, konieczne jest zaspokojenie trzech potrzeb, czyli:
</p>
<ul>
    <li>
    autonomii (określam cel, mogę wybrać sposoby jego osiągnięcia)
    </li>
    <li>
    kompetencji (chcę się rozwijać, mogę osiągnąć cel)
    </li>
    <li>
    przynależności (jestem częścią społeczności uczącej się) 
    </li>
</ul>
<p>
Motywacja wewnętrzna powoduje, że człowiek - niezależnie od wieku - chce podejmować
wyzwania, rozwijać się i poszukiwać rozwiązań adekwatnych do pojawiających się wyzwań.
To, co nauczyciel może robić z uczniami w swojej klasie w procesie wychowawczym lub
dydaktycznym, Ty jako dyrektor możesz robić ze swoją kadrą pedagogiczną. To wymaga
jednej odważnej decyzji: zaufania do drugiej strony oraz stworzenia warunków do rozwoju.
</p>
<p>
Jak możesz stworzyć warunki do rozwoju kadry pedagogicznej?
</p>
<ol>
    <li>Zadbaj o zaspokojenie potrzeb autonomii, kompetencji, przynależności:
        <ul>
            <li>dopóki Twoi nauczyciele nie działają na szkodę uczniów, pozwól im na poszukiwanie
    własnych metod pracy, rozmawiajcie wspólnie (z całą radą lub np. w zespołach
    przedmiotowych) o celach, jakie chcecie osiągnąć;</li>
    <li>
    komunikuj, jakie działania można podejmować w waszej szkole, a na jakie nie
    wyrażasz zgody (warto od razu podawać powody, dla których coś jest niemożliwe
    albo konieczne);
    </li>
    <li>
    buduj wśród nauczycieli kulturę pracy opartą na współpracy, a nie rywalizacji;
    </li>
    <li>
    traktuj błąd popełniony przez siebie lub innych jako okazję do wyciągania wniosków,
    jak uniknąć go w przyszłości w podobnej sytuacji.
    </li>
            </ul>
        </li>
        <li>
        Zachęcaj do autorefleksji
        </li>
</ol>
<p>
Holenderski badacz Fred Korthagen opracował tzw. “model cebuli”, który opisuje elementy
określające nauczyciela. Są to misja, tożsamość, przekonania, kompetencje, zachowanie oraz
środowisko. Nauczyciel powinien stale zadawać sobie pytania dotyczące jego
funkcjonowania w odniesieniu do tych warstw i powinien być świadomy, że warstwy te na
siebie bezpośrednio oddziaływują. Zewnętrzne warstwy wpływają na te wewnętrzne, np.
środowisko zmienia jego zachowanie (czyli np. panująca atmosfera kontroli i wrogości zmieni
sposób funkcjonowania nauczyciela). Z drugiej strony zachowanie wynika z jego przekonań.
Najbardziej fundamentalną warstwą jest ta wewnętrzna - misja i tożsamość. Wszelka zmiana
rozwojowa nauczyciela wynika z jego wnętrza, a następnie uzależniona jest od przepływu
między warstwami. Co zrobić, żeby uruchomić proces zmiany? Należy zadbać o trzy
podstawowe potrzeby opisane w punkcie 1:</p>
Podstawowe pytania autorefleksji:
<ul>
    <li>misja
        <ul>
        <li>W co wierzę? Jakie wartości są dla mnie ważne?</li>
        </ul>
    </li>
    <li>
        tożsamość
        <ul>
            <li>
            Kim jestem? Jakim jestem nauczycielem? Jakim chcę być nauczycielem?
            </li>
        </ul>
    </li>
    <li>
    przekonania:
    <ul>
        <li>
        W jaką edukację wierzę? Czym jest dla mnie uczenie, nauczanie? Co oznacza
dla mnie wiedza?
        </li>
    </ul>
    </li>
    <li>
    kompetencje
    <ul>
        <li>
        W czym jest dobry/a? Co umiem? Jak wiedzę posiadam?
        </li>
    </ul>
    </li>
    <li>
    zachowanie
    <ul>
        <li>
        Jak uczę? Co robię, aby osiągać cele dydaktyczne i wychowawcze?
        </li>
    </ul>
    </li>
    <li>
    środowisko
    <ul>
        <li>
        Z kim pracuję? Co napotkam w pracy? Jakie mam warunki pracy?
        </li>
    </ul>
    </li>
</ul>
